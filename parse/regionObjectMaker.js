const makeRegionObject = () => {
    const region = {
        name: regionName,
        coordinates: makeCoordinateArray(),
    }
    fs.writeFile(`${regionName}.json`, JSON.stringify(region), function (err) {
        if (err) return console.log(err);
        else console.log("Done");
    });
}

